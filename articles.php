<h2>16 février 2010</h2><h3>Appréhender les enjeux</h3><p>Pour accepter d’entrer dans une démarche qui exige un certain investissement personnel, chacun doit avoir parfaitement conscience de l’importance des enjeux. Les principaux enjeux du développement durable se jouant à l’échelle planétaire (pauvreté, malnutrition, climat, eau, énergie, etc.), il est essentiel de bien les rendre intelligibles pour les élèves - et pour les adultes !</p>

<h2>4 septembre 2009</h2><h3>Où en est notre planète ?</h3><p>Des chiffres alarmants. D’innombrables constats prouvent chaque jour que notre mode de développement actuel n’est pas viable à long terme.Dans le monde : 800 millions de personnes, dont 200 millions d’enfants, souffrent de la faim (Atlas mondial du développement durable, A.-M. Sacquet, Ed. Autrement, 2002) ; 20% d’êtres humains vivant dans les pays riches se partagent 86% de la consommation mondiale totale (Dossier Johannesburg, fiche 3, Rép. Française) ; La banquise de l’Arctique a perdu 10 à 15% de sa surface depuis 1950 (Le climat entre nos mains, OFEFP, 2002).En Suisse : On bétonne chaque seconde un mètre carré de sol (Objectif qualité de vie, Georg, 2002) ; Si chaque habitant du globe avait une consommation comparable à celle d’une personne vivant en Suisse, il ne faudrait pas moins de trois planètes pour répondre à nos besoins (Environnement suisse, Statistiques et analyses, OFEFP).</p>

<h2>18 septembre 2008</h2><h3>Les trois composantes du développement durable</h3><p>Contrairement à ce que l’on croit trop souvent, le développement durable ne se limite pas à une prise de conscience écologique. Il vise à instaurer un meilleur équilibre entre les dimensions économique, sociale et environnementale. Trois préoccupations majeures. Une économie plus « efficiente » Il s’agit par exemple : de fabriquer des objets de façon plus « efficiente » - c’est-à-dire en consommant moins d’énergie et moins de ressources ; de concevoir des articles de meilleure qualité, donc utilisables plus longtemps ; de proposer aux consommateurs des produits locaux et de saison, donc nécessitant moins de transports. Une société plus équitable. Le but est notamment de tenir compte des besoins fondamentaux de ceux qui ne peuvent pas s’exprimer (exclus des pays défavorisés ou des pays riches, générations futures, etc.). Un plus grand respect de l’environnement. Les principes du développement durable doivent permettre de préserver la richesse de la biodiversité, réduire la consommation de ressources naturelles, limiter les sources de pollution, réduire le volume de déchets, etc. « Des changements fondamentaux dans la façon dont les sociétés produisent et consomment sont indispensables pour réaliser un développement durable à l’échelle mondiale » (Rapport du Sommet mondial pour le développement durable, Johannesburg 2002). Une question de solidarité. Dans la recherche de cet équilibre, deux conditions s’avèrent indispensables : une solidarité spatiale, c’est-à-dire un partage équitable des ressources disponibles entre les différentes nations et les différentes catégories de population au sein d’un même pays ; une solidarité temporelle, c’est-à-dire le souci de satisfaire nos besoins actuels sans mettre en danger le bien-être et la survie des générations futures.	</p>
