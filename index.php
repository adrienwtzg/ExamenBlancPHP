                    <h2 class="title">Développement durable</h2>
                    <div class="story">
                        <h3>L’éducation au développement durable (EDD), c’est...</h3>
                        <ul>

                            <li>une question de valeurs ayant pour centre la notion de respect, comme le stipule le texte de l’UNESCO sur la Décennie de l’EDD : <blockquote><p>&#171; Respect des autres, qu’ils appartiennent aux générations actuelles ou futures, respect de la différence et de la diversité, de l’environnement, des ressources de la planète que nous habitons. &#187;</p></blockquote></li>

                            <li>une prise en compte des finalités de l’EDD par tous les acteurs de l’école, et pas seulement par les enseignants des disciplines spécifiquement concernées par les thèmes d’étude ; en ce sens, on dit que l’EDD est holistique. C’est l’ensemble d’un établissement scolaire qui devrait se sentir concerné si l’on y cherche vraiment à développer de telles valeurs et à provoquer des changements dans les attitudes.</li>

                            <li>une éducation inter- ou transdisciplinaire par essence, car les pratiques visant un développement durable ne sont possibles que par une action concertée des acteurs économiques, sociaux et environnementaux et par une compréhension globale des enjeux. Mais il n’y a pas de disciplines directement concernées par les objets d’étude qui sont en jeu et toutes peuvent être impliquées dans un projet : une étude sur l’effet de serre, par exemple, fera intervenir des connaissances en sciences humaines et expérimentales ; ces savoirs spécifiques pourront se mettre au service d’un projet d’information et de sensibilisation au sein de l’établissement, faisant intervenir des disciplines linguistiques et artistiques.</li>

                            <li>une mise en relation de l’école au monde pour donner ainsi plus de sens aux apprentissages.</li>
                        </ul>
                        <h3>EDD et Agenda 21 d’établissement scolaire</h3>

                        <p>L’Agenda 21 d’établissement scolaire est l’un des outils de l’EDD, puisqu’il permet d’élargir la dimension de participation citoyenne à tous les acteurs au sein de l’établisseement. Susciter la réflexion et encourager le changement de comportement si nécessaire : ainsi pourrait se résumer l’objectif concret de l’Agenda 21 d’établissement scolaire.</p>
